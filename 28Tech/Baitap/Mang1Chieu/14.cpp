#include <bits/stdc++.h>
using namespace std;
using ll = long long;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        int arr[n];
        for (int i = 0; i < n; i++) {
            cin >> arr[i];
        }

        int ans = arr[0];
        for (int i = 1; i < n; i++) {
            ans ^= arr[i];
        }

        cout << ans << '\n';
    }
    return 0;
}
